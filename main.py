try:
    # Запрашиваем у пользователя ввод двух чисел
    num1 = float(input("Введите первое число и нажмите Enter  "))
    num2 = float(input("Введите второе число и нажмите Enter  "))

    result1 = num1 + num2 # Пытаемся сложить первое число и второе
    print(f"Результат сложения: {result1}") # вставляем {значения переменных} прямо внутрь строки

    result2 = num1 - num2 # Пытаемся вычесть из первого число второе
    print(f"Результат вычитаия: {result2}") # вставляем {значения переменных} прямо внутрь строки

    result3 = num1 * num2 # Пытаемся умножить первое число на второе
    print(f"Результат умножения: {result3}") # вставляем {значения переменных} прямо внутрь строки

    result4 = num1 / num2 # Пытаемся разделить первое число на второе
    print(f"Результат деления: {result4}") # вставляем {значения переменных} прямо внутрь строки

except ValueError:
    print("Ошибка: введено не число. В следующий раз введите, пожалуйста, число!")
except ZeroDivisionError:
    print("Ошибка: деление на ноль недопустимо!")
finally:
    print("Работа программы завершена")